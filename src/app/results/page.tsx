function parseScoresFromText(text: string): { communication: number; aesthetic: number; drive: number; structure: number } | null {
  const scoresMatch = text.match(/## Scores[\s\S]*$/);
  if (!scoresMatch) return null;
  const scores: Record<string, number> = {};
  const lines = scoresMatch[0].split("\n");
  for (const line of lines) {
    const match = line.match(/(\w+):\s*(\d+)/);
    if (match) {
      const key = match[1].toLowerCase();
      scores[key] = parseInt(match[2], 10);
    }
  }
  if (scores.communication !== undefined && scores.aesthetic !== undefined && scores.drive !== undefined && scores.structure !== undefined) {
    return scores as { communication: number; aesthetic: number; drive: number; structure: number };
  }
  return null;
}

export default function ResultsPage() {
  const [audit, setAudit] = useState("");
  const [scores, setScores] = useState<{ communication: number; aesthetic: number; drive: number; structure: number } | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState("");
  const [isDownloading, setIsDownloading] = useState(false);
  const [auditId, setAuditId] = useState<string | undefined>();
  const [logoUrl, setLogoUrl] = useState<string | null>(null);
  const [feedbackSent, setFeedbackSent] = useState(false);
  const [showSubmerged, setShowSubmerged] = useState(false);
  const reportRef = useRef<HTMLDivElement>(null);
  const { data: session } = useSession();

  useEffect(() => {
    if (session?.user?.email) {
      fetch("/api/settings/branding")
        .then(res => res.json())
        .then(data => {
          if (data.customLogoUrl) setLogoUrl(data.customLogoUrl);
        })
        .catch(() => {});
    }
  }, [session]);

  const handleFeedback = async (score: number) => {
    if (!auditId) return;
    try {
      await fetch("/api/audit/feedback", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ auditId, score }),
      });
      setFeedbackSent(true);
    } catch (err) {
      console.error("Failed to send feedback", err);
    }
  };

  const downloadPDF = async () => {
    if (!reportRef.current) return;
    setIsDownloading(true);

    try {
      const scoresList = scores
        ? `<div style="display:grid;grid-template-columns:1fr 1fr;gap:12px;margin-bottom:32px">
            ${["Communication (Mercury)", "Aesthetic (Venus)", "Drive (Mars)", "Structure (Saturn)"].map((label, i) => {
              const key = ["communication", "aesthetic", "drive", "structure"][i];
              const val = scores[key as keyof typeof scores] || 0;
              return `<div style="padding:12px;border:1px solid #ddd;border-radius:8px">
                <div style="font-size:12px;color:#666;margin-bottom:4px">${label}</div>
                <div style="font-size:24px;font-weight:700">${val}/100</div>
                <div style="height:6px;background:#eee;border-radius:3px;margin-top:8px">
                  <div style="height:100%;width:${val}%;background:linear-gradient(90deg,#7000ff,#00d4ff);border-radius:3px"></div>
                </div>
              </div>`;
            }).join("")}
          </div>`
        : "";

      const html = `<!DOCTYPE html><html><head>
        <title>Cosmic Growth Audit</title>
        <style>
          body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif; max-width: 800px; margin: 0 auto; padding: 40px; color: #1a1a1a; line-height: 1.6; }
          h1, h2, h3 { color: #111; }
          h1 { font-size: 28px; border-bottom: 2px solid #7000ff; padding-bottom: 8px; }
          h2 { font-size: 20px; margin-top: 32px; }
          ul, ol { padding-left: 24px; }
          li { margin-bottom: 8px; }
          strong { color: #7000ff; }
          .branding { text-align: right; margin-bottom: 20px; }
          .branding img { max-height: 50px; }
          @media print { body { padding: 20px; } }
        </style>
      </head><body>
        ${logoUrl ? `<div class="branding"><img src="${logoUrl}" alt="Agency Logo" /></div>` : ""}
        <h1>Avditor Mvndi — Cosmic Audit Report</h1>
        ${scoresList}
        <div class="audit-content">${audit}</div>
        <hr style="margin-top:40px;border:none;border-top:1px solid #ddd"/>
        <p style="color:#999;font-size:12px;text-align:center">Generated by Avditor Mvndi • specvla-ergon--avditor-mvndi.vercel.app</p>
      </body></html>`;

      const response = await fetch("/api/pdf", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ html, filename: "cosmic-growth-audit.pdf" }),
      });

      if (!response.ok) throw new Error("Failed to generate PDF");

      const blob = await response.blob();
      const url = window.URL.createObjectURL(blob);
      const a = document.createElement("a");
      a.href = url;
      a.download = "cosmic-growth-audit.pdf";
      document.body.appendChild(a);
      a.click();
      a.remove();
      window.URL.revokeObjectURL(url);
    } catch (err) {
      console.error("PDF Generation failed", err);
      alert("Failed to generate PDF. Please try again.");
    } finally {
      setIsDownloading(false);
    }
  };

  useEffect(() => {
    const runAudit = async () => {
      const cachedResult = sessionStorage.getItem("current_audit_result");
      const cachedScores = sessionStorage.getItem("current_audit_scores");
      const cachedAuditId = sessionStorage.getItem("current_audit_id");
      if (cachedResult && cachedScores) {
        setAudit(cachedResult);
        setScores(JSON.parse(cachedScores));
        if (cachedAuditId) setAuditId(cachedAuditId);
        setLoading(false);
        return;
      }

      const data = sessionStorage.getItem("current_audit_request");
      if (!data) {
        setError("No audit data found. Please start from the home page.");
        setLoading(false);
        return;
      }

      const apiKey = getStoredApiKey(); // allow-secret
      if (!apiKey) {
        setError("API Key not found. Please configure it in settings.");
        setLoading(false);
        return;
      }

      const parsedData = JSON.parse(data);
      const requestHeaders: Record<string, string> = {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${apiKey}`,
        "X-AI-Provider": getStoredProvider(),
      };
      const requestBody = JSON.stringify(parsedData);

      try {
        let streamSuccess = false;
        try {
          const streamResponse = await fetch("/api/audit/stream", {
            method: "POST",
            headers: requestHeaders,
            body: requestBody,
          });

          if (!streamResponse.ok) {
            const errorData = await streamResponse.json().catch(() => null);
            throw new Error(errorData?.error || `HTTP error! status: ${streamResponse.status}`);
          }

          if (!streamResponse.body) {
            throw new Error("No response body for streaming");
          }

          const streamedAuditId = streamResponse.headers.get("X-Audit-Id");
          if (streamedAuditId) {
            setAuditId(streamedAuditId);
            sessionStorage.setItem("current_audit_id", streamedAuditId);
          }

          const reader = streamResponse.body.getReader();
          const decoder = new TextDecoder();
          let fullText = "";

          setLoading(false);

          while (true) {
            const { done, value } = await reader.read();
            if (done) break;
            const chunk = decoder.decode(value, { stream: true });
            fullText += chunk;
            setAudit(fullText);
          }

          const parsedScores = parseScoresFromText(fullText);
          if (parsedScores) {
            setScores(parsedScores);
            sessionStorage.setItem("current_audit_scores", JSON.stringify(parsedScores));
          }

          sessionStorage.setItem("current_audit_result", fullText);
          streamSuccess = true;
        } catch (streamErr) {
          if (!loading) throw streamErr;
        }

        if (!streamSuccess) {
          const response = await fetch("/api/audit", {
            method: "POST",
            headers: requestHeaders,
            body: requestBody,
          });

          const result = await response.json();
          if (!response.ok || result.error) {
            throw new Error(result.error || `HTTP error! status: ${response.status}`);
          }

          setAudit(result.audit);
          setScores(result.scores);
          if (result.id) {
            setAuditId(result.id);
            sessionStorage.setItem("current_audit_id", result.id);
          }
          sessionStorage.setItem("current_audit_result", result.audit);
          sessionStorage.setItem("current_audit_scores", JSON.stringify(result.scores));
        }
      } catch (err: unknown) {
        setError(err instanceof Error ? err.message : "Failed to generate audit.");
      } finally {
        setLoading(false);
      }
    };

    runAudit();
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  if (loading) {
    return (
      <main className="main" aria-live="polite" aria-busy="true">
        <div className="hero">
          <div className="astro-badge" style={{ animation: "pulse-slow 2s infinite" }}>
            <span aria-hidden="true">✦</span>
            Aligning with the Stars
          </div>
          <h1>Calculating Cosmic ROI...</h1>
          <p>Analyzing bottlenecks and identifying expansion windows for your brand.</p>
        </div>
        <div className="card" style={{ textAlign: "center", padding: "5rem" }}>
          <Loader />
        </div>
      </main>
    );
  }

  if (error) {
    return (
      <main className="main" aria-live="assertive">
        <div className="card" style={{ borderColor: "var(--accent)" }}>
          <h2 style={{ color: "var(--secondary)", marginBottom: "1rem" }}>Retrograde Detected</h2>
          <p role="alert">{error}</p>
          <Link href="/" className="btn" style={{ marginTop: "2rem", display: "block", textAlign: "center" }}>
            Try Again
          </Link>
        </div>
      </main>
    );
  }

  return (
    <main className="main" aria-live="polite">
      <div className="hero">
        <div className="astro-badge">
          <span aria-hidden="true">✦</span>
          Audit Manifested {(session?.user?.isPro || session?.user?.isAdmin) && <ProBadge />}
        </div>
        <h1 style={{ letterSpacing: "-0.06em", fontWeight: 900 }}>Strategic Alignment</h1>
        <p>A procedural identity derived from your digital footprint.</p>
        
        {auditId && <div style={{ marginTop: "1rem" }}>
          <ShareButtons
            url={`/share/${auditId}`}
            title="Cosmic Growth Audit"
            score={scores ? Math.round((scores.communication + scores.aesthetic + scores.drive + scores.structure) / 4) : 0}
          />
        </div>}
      </div>

      <div className="container" style={{ width: "100%", maxWidth: "900px" }}>
        
        {/* THE SINGLE HERO CARD: Alignment Sigil */}
        <section style={{ marginBottom: "4rem" }}>
          {scores && <AlignmentSigil scores={scores} />}
        </section>

        {/* SUBMERGED REPORT: Progressive Revelation */}
        <section style={{ marginBottom: "4rem" }}>
          <button 
            onClick={() => setShowSubmerged(!showSubmerged)}
            className="btn btn-secondary"
            style={{ width: "100%", marginBottom: "1.5rem", background: "rgba(255,255,255,0.03)", borderRadius: "100px", padding: "1.25rem" }}
          >
            {showSubmerged ? "↑ Submerge Detailed Intelligence" : "↓ Explore Submerged Strategic Data"}
          </button>

          {showSubmerged && (
            <div 
              ref={reportRef} 
              className="card" 
              style={{ 
                animation: "fadeIn 0.6s cubic-bezier(0.23, 1, 0.32, 1)", 
                padding: "4rem 3rem",
                background: "rgba(0,0,0,0.5)"
              }}
            >
              <div className="audit-content">
                <ReactMarkdown>{audit}</ReactMarkdown>
              </div>
              <div style={{ marginTop: "4rem", textAlign: "center" }}>
                <button onClick={downloadPDF} disabled={isDownloading} className="btn" style={{ maxWidth: "300px", borderRadius: "100px" }}>
                  {isDownloading ? "Generating PDF..." : "Export Strategic Manifest"}
                </button>
              </div>
            </div>
          )}
        </section>

        <EmailGate auditId={auditId}>
          <div style={{ textAlign: "center", marginBottom: "4rem" }}>
            <p style={{ fontSize: "0.85rem", color: "var(--text-muted)", marginBottom: "1.5rem", opacity: 0.6 }}>
              {feedbackSent ? "Data alignment recorded. ✦" : "Was this strategic disclosure helpful?"}
            </p>
            {!feedbackSent && (
              <div style={{ display: "flex", justifyContent: "center", gap: "1.5rem" }}>
                <button onClick={() => handleFeedback(1)} className="btn-secondary" style={{ padding: "0.5rem 2rem", fontSize: "1.2rem", background: "rgba(255,255,255,0.05)", minHeight: "44px", borderRadius: "50%" }}>👍</button>
                <button onClick={() => handleFeedback(0)} className="btn-secondary" style={{ padding: "0.5rem 2rem", fontSize: "1.2rem", background: "rgba(255,255,255,0.05)", minHeight: "44px", borderRadius: "50%" }}>👎</button>
              </div>
            )}
          </div>
        </EmailGate>

        <ChatBox auditContext={audit} />

        <div className="upsell-section" style={{ textAlign: "center", marginBottom: "8rem" }}>
          <h2 style={{ marginBottom: "3rem", fontSize: "2.5rem", letterSpacing: "-0.06em", fontWeight: 900 }}>Paths to Manifestation</h2>
          <div style={{ display: "flex", flexDirection: "column", gap: "1.5rem" }}>
            <SignalPathNode 
              pathNumber={1} 
              title="The Builder" 
              icon="hammer"
              color="#7000ff"
              description="Direct expert execution. We implement the submerged strategy for you to ensure perfect technical and creative alignment. Full disclosure of proprietary playbooks included." 
              buttonText="Inquire for Execution" 
              checkoutPathNumber={1}
              isPrimary={true} 
            />
            <SignalPathNode 
              pathNumber={2} 
              title="The Vault" 
              icon="key"
              color="#00d4ff"
              description="Access proprietary templates and actionable blueprints. This high-density resource is gated behind our professional alignment tier." 
              buttonText="Unlock the Vault" 
              checkoutPathNumber={2}
            />
            <SignalPathNode 
              pathNumber={3} 
              title="The Oracle" 
              icon="eye"
              color="#ffcc00"
              description="1-on-1 strategic diagnostic. Dive deep into the oceanic data with an expert to map your brand's unique growth window." 
              buttonText="Request Consultation" 
              checkoutPathNumber={3}
            />
          </div>
        </div>
      </div>

      <style jsx global>{`
        .audit-content h1, .audit-content h2, .audit-content h3 {
          margin-top: 3rem;
          margin-bottom: 1.5rem;
          color: #fff;
          letter-spacing: -0.04em;
          font-weight: 800;
        }
        @media (min-width: 769px) {
          .audit-content h1, .audit-content h2, .audit-content h3 {
            margin-top: 4rem;
            margin-bottom: 2rem;
          }
        }
        .audit-content h1:first-child, .audit-content h2:first-child {
          margin-top: 0;
        }
        .audit-content p {
          margin-bottom: 1.5rem;
          line-height: 1.8;
          color: rgba(255,255,255,0.8);
          font-size: 1.1rem;
        }
        .audit-content ul, .audit-content ol {
          margin-bottom: 2rem;
          padding-left: 1.5rem;
        }
        .audit-content li {
          margin-bottom: 1rem;
          color: rgba(255,255,255,0.6);
        }
        .audit-content strong {
          color: var(--secondary);
          font-weight: 700;
        }
      `}</style>
    </main>
  );
}
